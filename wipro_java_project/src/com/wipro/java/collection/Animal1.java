package com.wipro.java.collection;

public class Animal1 {

	private String name;
	private int age;
	
	public Animal1(String name, int age) {
		this.name = name;
		this.age = age;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @return the age
	 */
	public int getAge() {
		return age;
	}

}
