package com.wipro.java.collection;

public class Movie1 {
	
	private String n;
	private double r;
	private int y;
	
	public Movie1(String n, double r, int y) {
		this.n = n;
		this.r = r;
		this.y = y;
	}
	
	/**
	 * @return the n
	 */
	public String getN() {
		return n;
	}

	/**
	 * @return the r
	 */
	public double getR() {
		return r;
	}

	/**
	 * @return the y
	 */
	public int getY() {
		return y;
	}
	
}
